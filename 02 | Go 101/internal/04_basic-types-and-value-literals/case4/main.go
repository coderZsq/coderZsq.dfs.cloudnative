package main

func main() {
	_ = 1.23
	_ = 01.23 // == 1.23
	_ = .23
	_ = 1.
	// 一个e或者E随后的数值是指数值（底数为10）。
	// 指数值必须为一个可以带符号的十进制整数字面量。
	_ = 1.23e2  // == 123.0
	_ = 123E2   // == 12300.0
	_ = 123.E+2 // == 12300.0
	_ = 1e-1    // == 0.1
	_ = .1e0    // == 0.1
	_ = 0010e-2 // == 0.1
	_ = 0e+5    // == 0.0
}
