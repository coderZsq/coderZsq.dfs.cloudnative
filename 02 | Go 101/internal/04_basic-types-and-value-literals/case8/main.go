package main

func main() {
	_ = 1.23i
	_ = 1.i
	_ = .23i
	_ = 123i
	_ = 0123i   // == 123i（兼容性使然。见下）
	_ = 1.23E2i // == 123i
	_ = 1e-1i
	_ = 011i   // == 11i（兼容性使然。见下）
	_ = 00011i // == 11i（兼容性使然。见下）
	// 下面这几行从Go 1.13开始才能编译通过。
	_ = 0o11i    // == 9i
	_ = 0x11i    // == 17i
	_ = 0b11i    // == 3i
	_ = 0X.8p-0i // == 0.5i
}
